def analyse(datas, dicname = 'testdic'):
  precs = []
  recs = []
  f1s = []
  baccs = []
  for data in datas:
    precs.append(data[dicname]['prec'])
    recs.append(data[dicname]['rec'])
    f1s.append(data[dicname]['f1'])
    baccs.append(data[dicname]['bacc'])
  return precs, recs, f1s, baccs

