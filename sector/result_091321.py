def get_cols(r):
    e0 = []
    e1 = []
    e2 = []
    for item in r:
        e0.append(item[0]['f1'])
        e1.append(item[1]['f1'])
        e2.append(item[2]['f1'])
    return [np.average(e0), np.average(e1), np.average(e2)]


